    protected function validator(array $data)
    {
        return Validator::make($data, [
            'name' => ['required', 'string', 'max:255'],
            'email' => ['required', 'string', 'email', 'max:255', 'unique:users'],
            'phone' => ['required', 'string', 'max:255'],
            'password' => ['required', 'string', 'min:8', 'confirmed'],
            'file' => ['required', 'max:2000'], // set appropriate size limit for binary file
            'photo' => ['required', 'image'],
        ]);
    }

    /**
     * Create a new user instance after a valid registration.
     *
     * @param  array  $data
     * @return \App\Models\User
     */
    protected function create(array $data)
    {
        $photo = $data['photo'];
        $imageName="images/users/".time()."_".$photo->getClientOriginalName();
        $photo->move(public_path("images/users"),$imageName);

        $file = $data['file'];
        $filename="files/".time()."_".$file->getClientOriginalName();
        $file->move(public_path("files"),$filename);


            return User::create([
                'name' => $data['name'],
                'email' => $data['email'],
                'phone' => $data['phone'],
                'password' => Hash::make($data['password']),
                'file' => $filename,
                'photo' => $imageName,
            ]);
    }

}
